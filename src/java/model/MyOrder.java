package model;

/**
 *
 * @author LNV
 */
public class MyOrder {
    private int id;
    private String name;
    private String image;
    private int quantity;
    private int size;
    private double price;
    private String status;

    public MyOrder() {
    }

    public MyOrder(int id, String name, String image, int quantity, int size, double price, String status) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.quantity = quantity;
        this.size = size;
        this.price = price;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
